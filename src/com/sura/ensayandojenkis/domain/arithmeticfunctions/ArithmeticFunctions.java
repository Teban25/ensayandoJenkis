package com.sura.ensayandojenkis.domain.arithmeticfunctions;


public class ArithmeticFunctions {

	public ArithmeticFunctions() {
		
	}

	public int plus(int term1, int term2){
		return term1+term2;
	}

	public int subtraction(int term1, int term2) {
		return term1-term2;
	}

	public int multiply(int term1, int term2) {
		return term1*term2;
	}

	public double division(Double term1, Double term2) {
		return (term1/term2);
	}
	
	
	
}
